#!/bin/bash
#shell script to print a series using for loop


echo -n "Even Numbers :: "
for var in 0 2 4 6 8 10
do
	echo -n "$var "
done
echo -e "\n"
echo -n "Odd Numbers :: "
for var in 1 3 5 7 9 11
do
	echo -n "$var "
done
echo -e "\n"
echo -n "List of hardcoded values :: "
for var in 1 23 45 63 71 99 125 153 167 181 191 197 211 223 227 233 239 293
do 
	echo -n "$var "
done
echo -e "\n"
